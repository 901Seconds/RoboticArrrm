package net.tangentmc;

public class AngleTuple {
    double theta1,theta2;

    public AngleTuple(double theta1, double theta2) {
        this.theta1=theta1;
        this.theta2=theta2;
    }

    public double getTheta1() {
        return theta1;
    }
    public double getTheta2() {
        return theta2;
    }
}